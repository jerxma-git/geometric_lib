import math


def area(r):
    """Calculates the area of a circle

    Args:
        r (number): radius of the circle

    Returns:
        number: the area
    """
    return math.pi * r * r


def perimeter(r):
    """Calculates the perimeter of a circle

    Args:
        r (number): radius of the circle

    Returns:
        number: the perimeter
    """
    return 2 * math.pi * r

