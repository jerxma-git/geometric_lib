def area(a, b):
	"""Calculates the area of a rectangle

	Args:
		a (float): width of the rectangle
		b (float): height of the rectangle

	Returns:
		float: the area
	"""
	return a * b

def perimeter(a, b):
	"""Calculates the perimeter of a rectangle

	Args:
		a (float): width of the rectangle
		b (float): height of the rectangle

	Returns:
		float: _description_
	"""
	return 2 * (a + b)