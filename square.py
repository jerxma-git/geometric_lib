def area(a):
    """Calculates the area of a square

    Args:
        a (float): size of the square's side

    Returns:
        float: the area 
    """
    return a * a


def perimeter(a):
    """Calculates the perimeter of a square

    Args:
        a (float): size of the square's side

    Returns:
        float: the perimeter
    """
    return 4 * a
